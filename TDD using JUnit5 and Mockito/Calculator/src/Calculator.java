package com.cognizant.junitbasics;

/**
 * Exercise 1: Setting Up JUnit
 * ------------------------------------------------------------------
 * A simple class used to demonstrate that JUnit has been correctly
 * added to the project and a test class can successfully find,
 * compile against, and run against it.
 */
public class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}
